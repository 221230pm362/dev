package javaPrj02var;

public class Main {
	
	public static void main(String[] args) {
		
		/*
		 * 변수 : 변하는 수
		 * 
		 * - 초기화
		 * 변수타입 변수명 = 값;
		 * 
		 * - 변수 선언
		 * 변수타입 변수명;
		 * 
		 * - 값 할당
		 * 변수명 = 값;
		 *  
		 *  
		 * [변수 명명 규칙]
		 * - 숫자로 시작하면 안됨
		 * - 특수문자 X (단, _ 및 $ 은 가능)
		 * - 변수명 중복 X
		 * - 예약어 사용 X (예약어? int , float , long ...)
		 * - 띄어쓰기 X
		 * - camelCase 에 맞춰서 작성 (예외가 있긴 함)
		 */
		
		//초기화 (선언 + 할당)
		//int x = 10;
		
		//선언 (변수 만드는거)
		int x;
		
		//할당 (변수에 값을 넣는거)
		x = 10;
		
		System.out.println(x);

	}

}
